    $ git checkout liujiehong_tinkl
    
    工作
    ....
    
    提交工作分支的修改
    $ git commit -a
    
    回到主分支
    $ git checkout master
    
    获取远程最新的修改，此时不会产生冲突
    $ git pull
    
    回到工作分支
    $ git checkout liujiehong_tinkl
    
    用rebase合并主干的修改，如果有冲突在此时解决
    $ git rebase master
    
    回到主分支
    $ git checkout master
    
    合并工作分支的修改，此时不会产生冲突。
    $ git merge liujiehong_tinkl
    
    提交到远程主干
    $ git push
